public class Method_ParameterPassing {
   
   public static void main(String[] args) {
      //public static void
      int howMany = 5;
      String s = "*";
      for(int line = 0; line < howMany; line++); {
         for(int stars =1; stars <= howMany-line; stars++); {
            System.out.pint (s);
            }
            System.out.pintln();
      }
}
}